package ed25519

import (
	"testing"
)

func FuzzEd25519Validate(f *testing.F) {
	algo := ED25519()

	// Valid (msg, pubkey, sig) triplet from test suite
	f.Add(
		"test message",
		"ED4924A9045FE5ED8B22BAA7B6229A72A287CCF3EA287AADD3A032A24C0F008FA6",
		"C001CB8A9883497518917DD16391930F4FEE39CEA76C846CFF4330BA44ED19DC4730056C2C6D7452873DE8120A5023C6807135C6329A89A13BA1D476FE8E7100",
	)
	// Another valid signature from Sign tests (hello world with known key)
	f.Add(
		"hello world",
		"ED4924A9045FE5ED8B22BAA7B6229A72A287CCF3EA287AADD3A032A24C0F008FA6",
		"E83CAFEAF100793F0C6570D60C7447FF3A87E0DC0CAE9AD90EF0102860EC3BD1D20F432494021F3E19DAFF257A420CA64A49C283AB5AD00B6B0CEA1756151C01",
	)
	// All empty strings
	f.Add("", "", "")
	// Invalid hex in pubkey
	f.Add("test message", "ZZZZ", "C001CB8A9883497518917DD16391930F4FEE39CEA76C846CFF4330BA44ED19DC4730056C2C6D7452873DE8120A5023C6807135C6329A89A13BA1D476FE8E7100")
	// Invalid hex in sig
	f.Add("test message", "ED4924A9045FE5ED8B22BAA7B6229A72A287CCF3EA287AADD3A032A24C0F008FA6", "ZZZZ")
	// Wrong pubkey for valid sig
	f.Add("test message", "ED4924A9045FE5ED8B22BAA7B6229A72A287CCF3EA287AADD3A032A24C0F008FB6", "C001CB8A9883497518917DD16391930F4FEE39CEA76C846CFF4330BA44ED19DC4730056C2C6D7452873DE8120A5023C6807135C6329A89A13BA1D476FE8E7100")
	// Wrong message for valid sig/pubkey
	f.Add("wrong message", "ED4924A9045FE5ED8B22BAA7B6229A72A287CCF3EA287AADD3A032A24C0F008FA6", "C001CB8A9883497518917DD16391930F4FEE39CEA76C846CFF4330BA44ED19DC4730056C2C6D7452873DE8120A5023C6807135C6329A89A13BA1D476FE8E7100")

	f.Fuzz(func(t *testing.T, msg, pubkey, sig string) {
		// Must not panic, result is either true or false
		defer func() {
			if r := recover(); r != nil {
				t.Fatalf("Validate panicked: %v (msg=%q pubkey=%q sig=%q)", r, msg, pubkey, sig)
			}
		}()
		_ = algo.Validate(msg, pubkey, sig)
	})
}
