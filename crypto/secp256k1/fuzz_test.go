package secp256k1

import (
	"testing"
)

func FuzzSecp256k1Validate(f *testing.F) {
	algo := SECP256K1()

	// Valid (msg, pubkey, sig) triplets from test suite
	f.Add(
		"Hello World",
		"02950F4710101A25073BF37086D73FBBD00C7A6B0F91097D8F0BC6D268C400D56E",
		"3045022100E1617F1A3C85B5BC8FA6224F893FE9068BEA8F8D075EE144F6F9D255C829761802206FD9B361CDE83A0C3D5654232F1D7CFB1A614E9A8F9B1A861564029065516E64",
	)
	f.Add(
		"test",
		"02950F4710101A25073BF37086D73FBBD00C7A6B0F91097D8F0BC6D268C400D56E",
		"30450221008F0B50BEEA0C9787E85EEF9624E9385CCBE565B221BAEC2F2DA5F1D9D6D976F7022022C1B1829AE0E758FB690110F245F15433A0579C44910785FE75F93B9D0FB41F",
	)
	f.Add(
		"test message",
		"02950F4710101A25073BF37086D73FBBD00C7A6B0F91097D8F0BC6D268C400D56E",
		"30440220583A91C95E54E6A651C47BEC22744E0B101E2C4060E7B08F6341657DAD9BC3EE02207D1489C7395DB0188D3A56A977ECBA54B36FA9371B40319655B1B4429E33EF2D",
	)
	// All empty strings
	f.Add("", "", "")
	// Invalid hex in pubkey
	f.Add("Hello World", "ZZZZ", "3045022100E1617F1A3C85B5BC8FA6224F893FE9068BEA8F8D075EE144F6F9D255C829761802206FD9B361CDE83A0C3D5654232F1D7CFB1A614E9A8F9B1A861564029065516E64")
	// Invalid hex in sig
	f.Add("Hello World", "02950F4710101A25073BF37086D73FBBD00C7A6B0F91097D8F0BC6D268C400D56E", "ZZZZ")
	// Truncated pubkey
	f.Add("Hello World", "02950F4710101A25073BF37086D73FBBD00C7A6B0F91097D8F0BC6D268C400D5", "3045022100E1617F1A3C85B5BC8FA6224F893FE9068BEA8F8D075EE144F6F9D255C829761802206FD9B361CDE83A0C3D5654232F1D7CFB1A614E9A8F9B1A861564029065516E64")
	// Wrong message for valid sig/pubkey
	f.Add("wrong message", "02950F4710101A25073BF37086D73FBBD00C7A6B0F91097D8F0BC6D268C400D56E", "3045022100E1617F1A3C85B5BC8FA6224F893FE9068BEA8F8D075EE144F6F9D255C829761802206FD9B361CDE83A0C3D5654232F1D7CFB1A614E9A8F9B1A861564029065516E64")

	f.Fuzz(func(t *testing.T, msg, pubkey, sig string) {
		// Must not panic, result is either true or false
		_ = algo.Validate(msg, pubkey, sig)
	})
}
